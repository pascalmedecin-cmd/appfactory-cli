/** Avance max observée sur le sous-ensemble latin (« W ») : repli conservateur. */
export const OUTFIT_BOLD_MAX_ADVANCE = 1.002;

/** Largeur RÉELLE (pt) d'une chaîne en Outfit Bold à `size` pt (somme des avances mesurées). */
export function measureOutfitBold(s: string, size: number): number {
	let w = 0;
	for (const c of s) w += (OUTFIT_BOLD_ADVANCES[c] ?? OUTFIT_BOLD_MAX_ADVANCE) * size;
	return w;
}
